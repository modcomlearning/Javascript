//Arrow Function
const sayHello = () => {
    console.log("Hello, world!");
};

sayHello();  // Output: Hello, world!


// Arrow Function to Add 2 Numbers
const sum = () => {
    let num1 = 12;
    let num2 = 45;
    let answer = num1 + num2;
    console.log("Your Answer is "+answer);
}

// Call Function
sum();