// Arrow function for greetfunction
const greet = (name) => {
    console.log("Hello, " + name + "!");
};

greet("Alice");  // Output: Hello, Alice!
greet("Bob");    // Output: Hello, Bob!



// Arrow function for My County
const myCounty = (county_name) => {
    console.log("My County is " + county_name);
};

myCounty("Kiambu");
myCounty("Nakuru");
myCounty("Kitui");



// Arrow function to Add 2 Numbers
const sum = (num1, num2) => {
    let answer = num1 + num2;
    console.log("Your Answer is "+answer);
};

sum(12, 14);
